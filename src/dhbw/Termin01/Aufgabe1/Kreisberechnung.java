package dhbw.Termin01.Aufgabe1;


public class Kreisberechnung
{

    public static double durchmesser(double r)
    {
        return 2 * r;
    }

    public static double umfang(double r)
    {
        return 2 * r * Math.PI;
    }
    public static double flaecheninhalt(double r)
    {
        return r * r * Math.PI;
    }


}
